		function getTime(time){
			var m=parseInt(time/60)<=9?'0'+parseInt(time/60):parseInt(time/60);
			var s=parseInt(time-m*60)<=9?'0'+parseInt(time-m*60):parseInt(time-m*60);
			return m+':'+s;
		}
